package com.malfaang.e_culture_tool_a.routes;

//Classe per il canvas
public class AreaCanvas {

    private int width;
    private int height;

    public  AreaCanvas(int width, int height ){
        this.width = width;
        this.height = height;
    }

    public int getWidth(){
        return width;
    }

    public int getHeight(){
        return height;
    }

}
